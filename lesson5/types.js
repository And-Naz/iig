// Data Types
console.log(1, 1.3, 0.2e+2, 1_000_000, NaN, typeof 0, typeof NaN); // number
console.log(1n, typeof 5n); // bigint
console.log(true, false, typeof true); // boolean
console.log(null, typeof null); // null
console.log(undefined, typeof undefined); // undefined
console.log("fsdfgs", 'dfsgsdfg', `fgsdfgsd`, `dghd-${ 100 }-dgfhdfg`, typeof 'g'); // string
console.log(Symbol("inch uzes"), typeof Symbol("inch uzes")); // symbol
console.log({ id: 1, price: 500 }, typeof { id: 1, price: 500 }); // object





